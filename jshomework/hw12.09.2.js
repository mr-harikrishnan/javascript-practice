function getDayName(n) {
    switch (n) {
        case 1:
            console.log("sunday");
            break;

        case 2:
            console.log("monday");
            break;

        case 3:
            console.log("Tuesday");
            break;

        case 4:
            console.log("Wednesday");
            break;

        case 5:
            console.log("Thursday");
            break;

        case 6:
            console.log("Friday");
            break;

        case 7:
            console.log("Saturday");
            break;

        default:
            console.log("error");
            break;

    }
}

getDayName(6);

